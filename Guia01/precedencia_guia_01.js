console.log( 7 + 7 + 7 );   // retorna 21
console.log( 7 + 7 + "7" ); // retorna 147 (el ultimo + lo usa como concatenacion)
console.log( "7" + 7 + 7 ); // retorna 777 (castea los dos ultimos 7 a string)
console.log( 10 - 2 - 4 );  // retorna 4
console.log( 10 - 2 - "4" );// retorna 4 (castea el ultimo 4 a numero)
console.log( "10" - 2 - 4 ); // retorna 4 (castea el 10 a number por haber un signo -)
